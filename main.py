        await query.edit_message_text("–ü–∞–º—è—Ç–∫–∞: –°–° - —Å–æ–ª–Ω–µ—á–Ω–æ–µ —Å–ø–ª–µ—Ç–µ–Ω–∏–µ, –¢–† - —Ç—Ä–∞—Ö–µ—è, –î–ó - –≥–æ–ª–æ–≤–∞, –ì–î–ù - –Ω–∏–∂–µ –ø–æ—è—Å–∞.")
